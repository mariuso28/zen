package org.zen.json;

public class ActionJson {

	private String paymentsPending="";
	private String upgradable="";
	
	public ActionJson()
	{
	}

	public String getPaymentsPending() {
		return paymentsPending;
	}

	public void setPaymentsPending(String paymentsPending) {
		this.paymentsPending = paymentsPending;
	}

	public String getUpgradable() {
		return upgradable;
	}

	public void setUpgradable(String upgradable) {
		this.upgradable = upgradable;
	}
	
	
}
