package org.zen.json;

public class CountryJson {

	private String code;
	private String country;
	
	public CountryJson()
	{
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}
	
	
}
